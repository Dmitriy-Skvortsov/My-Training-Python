# 1.Напишите программу для вычисления количества продаж и среднего чека в интервалах: 
# a. До 1000 р включительно
# b. От 1000 р до 3000 р включительно
# c. От 3000 р до 5000 р включительно
# d. Выше 5000 р.
# 2.Список продаж: sales = [2033, 300.50, 1550, 2703, 5110, 2146, 4733, 7114, 1614, 3105, 3155, 853, 10114, 1315]
